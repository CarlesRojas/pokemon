const findPokeByNumber = (value: number): Poke | undefined => {
    for (const [key, val] of Object.entries(PokemonId)) {
        if (val === value) return key as Poke;
    }

    return undefined;
};

const pokemonToJson = (pokemon: PokemonInfo): string => {
    return `{
        id: ${pokemon.id},
        name: "${pokemon.name}",
        ${pokemon.form ? `form: "${pokemon.form}",` : ""}
        forms: [${pokemon.forms.map((form: PokemonInfo) => pokemonToJson(form))}],
        types: [${pokemon.types.map((type: Type) => `Type.${type}`).join(", ")}],
        total: ${pokemon.total},
        hp: ${pokemon.hp},
        attack: ${pokemon.attack},
        defense: ${pokemon.defense},
        specialAttack: ${pokemon.specialAttack},
        specialDefense: ${pokemon.specialDefense},
        speed: ${pokemon.speed},
        generation: ${pokemon.generation},
        spriteSize: new Vector2(${pokemon.spriteSize.x}, ${pokemon.spriteSize.y})
    }`;
};

export const pokemonData: Partial<Record<Poke, PokemonInfo>> = {};

pokemonJson.forEach((pokemon: any) => {
    const id: number = pokemon.id;
    const enumName = findPokeByNumber(id);

    if (!enumName) {
        console.log(`${pokemon.id} ${pokemon.name}`);
        return;
    }

    const types = [pokemon.type1, pokemon.type2].filter((type) => type && type !== "").map((type) => type.toUpperCase() as Type);
    const pokemonInfo: PokemonInfo = {
        id: pokemon.id,
        name: pokemon.name,
        forms: [],
        types: types,
        total: pokemon.total,
        hp: pokemon.hp,
        attack: pokemon.attack,
        defense: pokemon.defense,
        specialAttack: pokemon["special-attack"],
        specialDefense: pokemon["special-defense"],
        speed: pokemon.speed,
        generation: pokemon.generation,
        spriteSize: new Vector2(2, 2),
    };
    if (pokemon.form) pokemonInfo.form = pokemon.form;

    if (pokemonData[enumName]) pokemonData[enumName].forms.push(pokemonInfo);
    else pokemonData[enumName] = pokemonInfo;
});

const output = `
export const pokemonData: Record<Poke, PokemonInfo> = {
    ${Object.entries(pokemonData)
        .map(([key, value]) => `[Poke.${key}]: ${pokemonToJson(value)},`)
        .join("\n")}
};
`;

fs.writeFileSync("src/game/data/pokemonNewData.ts", output);
